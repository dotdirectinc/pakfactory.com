function postDoc({
  _id,
  title,
  slugCurrent,
  publishedAt,
  categoryId,
  authorId,
  excerpt,
  tagIds = [],
}) {
  return {
    _id,
    _type: 'post',
    language: 'en',
    title,
    slug: slug(slugCurrent),
    publishedAt,
    category: ref(categoryId),
    author: ref(authorId),
    excerpt,
    tldr: [block(excerpt)],
    body: [
      block(`${title} — development test copy for the PakFactory blog.`),
      block(
        'This document was generated by seed-blog-dev.mjs for local QA of the blog home, category rows, and post detail routes.',
      ),
    ],
    ...(tagIds.length ? { tags: tagIds.map((id) => ref(id)) } : {}),
    metaTitle: `${title} | PakFactory Blog`,
    metaDescription: excerpt,
  }
}

